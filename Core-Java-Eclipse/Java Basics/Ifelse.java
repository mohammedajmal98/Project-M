
public class Ifelse {

	public static void main(String[] args) {

		int x = 0;

		if (x == 0) {
			System.out.println("Neither even nor odd");
		} 
		
		else if (x % 2 == 0) {
			System.out.println("It is even");
		} 
		
		else {
			System.out.println("It is odd");
		}

	}

}
