
public class TypeCasting {

	public static void main(String[] args) {
		
		int a;
		byte b;
		a=10;
		b=(byte)a;
		System.out.println(b);
		
		char c='a';
		double d = 99;
		c=(char)d;
		System.out.println(c);
		
		boolean e = false;
		int f;
		f=c;
		System.out.println(f);
		

	}

}
