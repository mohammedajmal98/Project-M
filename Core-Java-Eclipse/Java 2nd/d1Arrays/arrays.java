package d1Arrays;

public class arrays {

	public static void main(String[] args) {
		
		String marks[];    //now 'memory/space' is not allocated
		
		marks = new String[5];  // now 'memory' is allocated
		//(or) int marks[] = new int[5]
		
		int failList[] = {2,4,6,8,9};
		
		System.out.println(failList);
		System.out.println(failList[4]);
		System.out.println(marks[0]); // initialize as 'null' for 'String', '0' for int if no value is given
		
//		index (or) subscript
		
		
		

	}

}
