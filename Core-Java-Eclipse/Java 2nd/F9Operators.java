
public class F9Operators {

	public static void main(String[] args) {
		
		//Arithmetic Operators --> +,-,*,/,%
		//Compound Assignment --> a+=1 --> a= a+1 and a+=b --> a=a+b
		
		//Increment/Decrement operators --> ++x,x++/--x,x--   pre/post
		int a=3,b=4, c;
		c = a + b + a++ + b++ + ++a + ++b; // 3 + 4 + 3 + 4 + 5 + 6
		System.out.println(c);
		
		//Relational Operators --> <,<=,>,>=,==,!=
		System.out.println(a>b);
		
				
		

	}

}
